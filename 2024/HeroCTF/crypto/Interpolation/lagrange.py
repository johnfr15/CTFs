# Interpolation function
def lagrange_interpolation(points):
    """ Lagrange interpolation to recover the polynomial coefficients. """
    x = R.gen()  # Define x in the polynomial ring
    f = 0
    n = len(points)
    
    for i in range(n):
        x_i, y_i = points[i]
        L_i = 1
        
        for j in range(n):
            if i != j:
                x_j, _ = points[j]
                L_i *= (x - x_j) / (x_i - x_j)
        
        f += y_i * L_i
    return f

